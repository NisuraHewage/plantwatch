CREATE TABLE PlantProfiles (Id INT AUTO_INCREMENT  PRIMARY KEY, Name Varchar(300), ScientificName Varchar(300),
ImageUrl Varchar(300), PlantDescription varchar(1000), 
 Watering varchar(1000),
 Temperature varchar(1000),
 Sunlight varchar(1000),
 Soil varchar(1000),
 Pests varchar(1000),
 Diseases varchar(1000),
 Fertilizer varchar(1000));